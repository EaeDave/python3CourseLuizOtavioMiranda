"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)  # Tipagem dinâmica, o Python já sabe que o valor 1234 é do tipo inteiro, ou int.

# Aspas simples
print('Luiz Otávio')  # Sabe que é do tipo string ou str.
print(1, 'Luiz "Otávio"')

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'")

# Escape
# É utilizado o caractere de escape para exibir as aspas duplas em uma string já usanda as aspas em seu corpo.
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio\"")
